//indexOf lastIndexOf  返回索引或者-1
//参数：要查找的元素，开始查找的位置
var arr = [2,5,67,'2',3,2,45];
//找数组中有没有2
console.log(arr.indexOf(2)); //0
console.log(arr.indexOf(2,1)); //5
console.log(arr.indexOf(22)); //-1

console.log(arr.lastIndexOf(2)); //5
console.log(arr.lastIndexOf(2,3));//0

