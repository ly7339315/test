//分支语句 switch 开关语句
//判断星期几 1-7
var num = 33;
var result = '';
switch(num){
	case 1:
		result = '星期一';
		break;
	case 2:
		result = '星期二';
		break;
	case 3:
		result = '星期三';
		console.log(3);
		break;
	case 4:
		result = '星期四';
		console.log(4);
		break;
	case 5:
		result = '星期五';
		console.log(5);
		break;
	case 6:
		result = '星期六';
		break;
	case 7:
		result = '星期七';
		break;
	default:
		result = '数字出现错误';
		break;
}
console.log(result);












